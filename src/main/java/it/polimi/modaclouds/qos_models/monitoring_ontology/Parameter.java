package it.polimi.modaclouds.qos_models.monitoring_ontology;

import java.net.URISyntaxException;

import it.polimi.modaclouds.monitoring.kb.api.KBEntity;

public class Parameter extends KBEntity{

	private String name;
	private String value;

	public Parameter() throws URISyntaxException{
		super();
	}
	
	public Parameter(String name, String value) throws URISyntaxException {
		super();
		this.setName(name);
		this.setValue(value);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		Parameter other = (Parameter) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}

	@Override
	public String getURIBase() {
		return MO.URI;
	}

	@Override
	public String getURIPrefix() {
		return MO.prefix;
	}

}
